package main.java.form;

public class FormData {
    private String names;
    private String roles;

    public String getNames() {
        return names;
    }

    public void setNames(String names) {
        this.names = names;
    }

    public String getRoles() {
        return roles;
    }

    public void setRoles(String content) {
        this.roles = content;
    }
}
